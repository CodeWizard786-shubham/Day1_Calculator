package javasamples;

public class JavaLoops {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("While loop");
        int i=0;
        while(i<10){
         System.out.println(i);
        }
    }
}
